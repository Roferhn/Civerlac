    private void btnIniciarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarRegistroActionPerformed
            int idUser;
            if(txtCorreoElctronicoRegistro.getText()!=""){
                if(state=="validar"){
                    ConexionSQL cn = new ConexionSQL();
                Connection con = null;
                PreparedStatement ps = null;
                ResultSet rs = null;
                String sql = "Select * from `empleado` WHERE idEmpleado=?";
                //String sql = "INSERT INTO `cliente`(`idCliente`, `Nombre`, `Correo`, `Direccion`, `idTelefonoCli`, `Estado`) VALUES (?,?,?,?,?,?)";
                double cantidad;
                try{
                    idUser=Integer.parseInt(  txtCorreoElctronicoRegistro.getText()  );
                    con = cn.getConnection();
                    ps = con.prepareStatement(sql);
                    ps.setString(1, txtCorreoElctronicoRegistro.getText());
                    rs = ps.executeQuery();
                    if(rs.next()){
                        JOptionPane.showMessageDialog(null, "El usuario Existe");
                        txtNombreRegistro.setText(rs.getString("Nombre"));
                        
                        txtContrasenaRegistro.setEnabled(true);
                        btnIniciarRegistro.setText("Registrar");
                        state="registrar";
                        
                    }
                    
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(null, e.toString());

                }
                
                }
                else if(state=="registrar") {
                
                   try{
                        RegistroDB reg = new RegistroDB();
                        reg.RegistrarUsuario(Integer.parseInt(txtCorreoElctronicoRegistro.getText() ) ,txtContrasenaRegistro.getText() );
                   }
                   catch(Exception ex){
                       JOptionPane.showMessageDialog(null, "Al empleado ya se le asigno un usuario");
                   }
                }

            }
    }//GEN-LAST:event_btnIniciarRegistroActionPerformed

